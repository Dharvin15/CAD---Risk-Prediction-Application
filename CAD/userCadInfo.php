                        <p class="card-text" >Coronary Artery Disease (CAD), a blockage of the arteries that supply blood to the heart, is the most common type of heart disease. CAD affects about 18.2 million Americans aged 20 and older, and it killed nearly 366,000 in 2017 (Thomas, 2020).</p>

                        <p class="card-text">According to the Center for Disease Control and Prevention (CDC), almost half of all Americans have at least one risk factor for CAD. Some of the risk factors include high blood pressure, high cholesterol levels, smoking, obesity, and physical inactivity (Donovan, 2020). </p>

                        <p class="card-text">The coronary arteries supply blood, oxygen and nutrients to your heart. A buildup of plaque can narrow these arteries, decreasing blood flow to your heart. Eventually, the reduced blood flow may cause chest pain (angina), shortness of breath, or other related signs and symptoms. A complete blockage can cause a heart attack. Because CAD often develops over decades, you might not notice a problem until you have a significant blockage or a heart attack (www.mayoclinic.org, 2020).</p><br>
                        
